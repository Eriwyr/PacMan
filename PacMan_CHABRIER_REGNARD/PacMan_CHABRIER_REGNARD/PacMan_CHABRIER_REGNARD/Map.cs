        public void countBeans()
        {
            
            for(int i = 0; i < VX; i++)
            {
                for(int j = 0; j < VY; j++)
                {
                    Element elmt = this.checkElement(new Position(i, j));
                    if (elmt > Element.Wall && elmt < Element.Nothing)
                    {
                        nbBeans++;
                    }
                }
            }
        }

        public void removeBean()
        {
            nbBeans--;
        }

